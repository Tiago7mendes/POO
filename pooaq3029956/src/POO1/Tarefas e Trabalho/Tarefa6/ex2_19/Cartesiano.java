package ex2_19;

public class Cartesiano {
    public double pontoX;
    public double pontoY;

    public Cartesiano (double x, double y) {
        pontoX = x;
        pontoY = y;
    }

    public double getX() {
        return pontoX;
    }

    public double getY() {
        return pontoY;
    }
}
